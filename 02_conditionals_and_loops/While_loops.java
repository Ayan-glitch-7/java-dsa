
public class While_loops {  
    public static void main(String[] args) {
        
        /*
        SYANTAX OF WHILE-LOOP:
        while (condition) {
            // body
        }
        */

        // Q-print numbers from 1 to 10:
        int num = 1;
        while (num <= 10) {
            System.out.println(num);
            num++;
        }
    }
}
