public class Do_while_loops {
    public static void main(String[] args) {
        
        /*
        SYNTAX OF DO-WHILE-LOOP:
        do {
            //body
        }   while (condition);
        */   
  
        // Q-print numbers from 1 to n:
        int n = 1;
        do {
            System.out.println(n);
            n++;
        } while (n <= 10);
    }
}